from copy import deepcopy
from typing import Any, Optional


def clause(no: str, title: str, content: str = "") -> dict[str, str]:
    item = {"no": no, "title": title}
    if content:
        item["content"] = content
    return item


def standard(
    code: str,
    name: str,
    version: str = "现行",
    effective_date: str = "",
    clauses: Optional[list[dict[str, str]]] = None,
    status_note: str = "现行/适用时使用",
    source: str = "官方公开目录或主管部门公告",
) -> dict[str, Any]:
    return {
        "code": code,
        "name": name,
        "version": version,
        "effective_date": effective_date,
        "clauses": [
            clause("状态", status_note, source),
            *(clauses or [clause("审核要点", "按产品属性匹配适用范围、强制标示、限量和检测方法。")]),
        ],
    }


