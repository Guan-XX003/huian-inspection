def build_expanded_seed_standards(base: dict[str, list[dict[str, Any]]]) -> dict[str, list[dict[str, Any]]]:
    merged = deepcopy(base)
    for industry_code, additions in EXPANDED_LEGAL_CATALOG.items():
        current = merged.setdefault(industry_code, [])
        known = {item["code"] for item in current}
        for item in additions:
            if item["code"] not in known:
                current.append(item)
                known.add(item["code"])
    return merged
